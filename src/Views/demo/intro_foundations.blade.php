    <ul>
        <li>Scale is important and scale is very hard. There will be many pieces to solving it, but a critical foundation is to minimize friction, both for project leaders and for participants.</li>
    </ul>
    <li>Catalyze the Unexpected</li>
    <ul>
        <li>One of the biggest problems in government-citizen interactions is that organizers of the input tend to skew the results simply through the kinds of questions they ask and inputs they allow.</li>
        <li>A key design goal is to enable people to pose and answer and have others answer questions that we don't know to ask so that we have the opportunity to learn the things that we don't know we don't know.</li>
